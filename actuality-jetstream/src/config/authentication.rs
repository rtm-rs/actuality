//! Authentication Options
//!
//! | Environment                      | Server Flag  | Description                                                                            |
//! |----------------------------------|--------------|----------------------------------------------------------------------------------------|
//! | `RTM_JS_SERVER_USER`             | `--user`     | Required username for connections (exclusive of `--auth`).                            |
//! | `RTM_JS_SERVER_PASS`             | `--pass`     | Required password for connections (exclusive of `--auth`).                            |
//! | `RTM_JS_SERVER_AUTH`             | `--auth`     | Required authorization token for connections (exclusive of `--user` and `--password`). |
