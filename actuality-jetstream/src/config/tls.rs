//! TLS Options
//!
//! | Environment                | Server Flag   | Description                                 |
//! |----------------------------|---------------|---------------------------------------------|
//! | `RTM_JS_SERVER_TLS`        | `--tls`       | Enable TLS, do not verify clients.          |
//! | `RTM_JS_SERVER_TLSCERT`    | `--tlscert`   | Server certificate file.                    |
//! | `RTM_JS_SERVER_TLSKEY`     | `--tlskey`    | Private key for server certificate.         |
//! | `RTM_JS_SERVER_TLSVERIFY`  | `--tlsverify` | Enable client TLS certificate verification. |
//! | `RTM_JS_SERVER_TLSCACERT`  | `--tlscacert` | Client certificate CA for verification.     |
